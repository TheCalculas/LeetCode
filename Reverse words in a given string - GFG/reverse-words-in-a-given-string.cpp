class Solution
{
    public:
    //Function to reverse words in a given string.
    string reverseWords(string s) 
    { 
        // code here 
        stringstream ss(s);
        
        string a = "";
        string temp = "";
        while(getline(ss, temp, '.'))
        {
            a = temp+"." + a;
            
        }
        a.pop_back();
        return a;
    } 
};

